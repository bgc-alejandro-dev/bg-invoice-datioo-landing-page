(function() {
  const STORAGE_KEY = 'datioo-lang';

  function getPreferredLang() {
    const saved = localStorage.getItem(STORAGE_KEY);
    if (saved && translations[saved]) return saved;
    const browserLang = navigator.language.slice(0, 2);
    return translations[browserLang] ? browserLang : 'en';
  }

  function applyTranslations(lang) {
    const t = translations[lang];
    if (!t) return;

    document.documentElement.lang = lang;

    // Update text content
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      if (t[key]) el.textContent = t[key];
    });

    // Update innerHTML (for elements with HTML like spans)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      const key = el.getAttribute('data-i18n-html');
      if (t[key]) el.innerHTML = t[key];
    });

    // Update placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      const key = el.getAttribute('data-i18n-ph');
      if (t[key]) el.placeholder = t[key];
    });

    // Update page title and meta
    if (t['meta.title']) document.title = t['meta.title'];
    const metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && t['meta.description']) metaDesc.setAttribute('content', t['meta.description']);

    // Update toggle buttons
    document.querySelectorAll('.lang-toggle').forEach(btn => {
      const label = btn.querySelector('.lang-toggle-label');
      if (label) label.textContent = lang === 'en' ? 'ES' : 'EN';
    });

    localStorage.setItem(STORAGE_KEY, lang);
  }

  function toggleLang() {
    const current = document.documentElement.lang || 'en';
    const next = current === 'en' ? 'es' : 'en';
    applyTranslations(next);
  }

  // Initialize on DOM ready
  document.addEventListener('DOMContentLoaded', () => {
    const lang = getPreferredLang();
    applyTranslations(lang);

    document.querySelectorAll('.lang-toggle').forEach(btn => {
      btn.addEventListener('click', toggleLang);
    });
  });

  // Expose globally
  window.datiooI18n = { applyTranslations, toggleLang, getPreferredLang };
})();
